                                dos actos associados a esta área, existe uma diversidade considerável de
                                referenciais e respectivas certificações profissionais, destacando-se os seguintes:
                                (i) o PMBOK (Project Management Body of Knowledge) promovido pelo PMI (Project
                                Management Institute) que atribui diversos tipos de certificações,
                                nomeadamente o PMP (Project Management Professional) e o PMI-ACP (PMI
                                Agile Certified Practitioner); (ii) a associação IPMA (International Project
                                Management Association) que atribui diversos níveis de certificação em gestão
                                de projectos; (iii) o PRINCE (Projects in Controlled Environments) e o Scrum
                                que são exemplos de abordagens/certificações que promovem práticas de gestão
                                de projectos especialmente dedicadas ao desenvolvimento de software.
