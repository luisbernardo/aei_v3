                                coordenação, medição, monitorização, controlo e documentação) aos contextos de
                                desenvolvimento de soluções informáticas, para garantir que o mesmo é
                                sistemático, disciplinado e quantificável. No caso da Engenharia Informática, a
                                gestão de projectos apresenta alguma especificidade, devido às particularidades da
                                tecnologia do software e do respectivo processo de desenvolvimento. Salienta-se,
                                nomeadamente, a natureza intangível dos artefactos resultantes do processo de
                                engenharia de software (que impõe a necessidade de raciocinar a níveis de
                                abstracção tendencialmente elevados) e à elevadíssima taxa de actualização
